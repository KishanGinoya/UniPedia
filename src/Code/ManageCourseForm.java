    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed

        try {
            String program = cb_program.getSelectedItem().toString();
            int sem = Integer.parseInt(cb_sem.getSelectedItem().toString());
            String c1 = cb_c1.getSelectedItem().toString();
            String c2 = cb_c2.getSelectedItem().toString();
            String c3 = cb_c3.getSelectedItem().toString();
            String c4 = cb_c4.getSelectedItem().toString();

            if (!c.checkProgramSem('i', 0, program, sem, c1, c2, c3, c4)) {
                c.insertUpdateDeleteCourse('i', null, program, sem, c1, c2, c3, c4);
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0); // Clear existing rows
                c.fillStudentJTable(jTable1);
                clearFields();
            } else {
                String errorMessage = "<html><b style='font-size: 12px;'>This Record Already Exist..<br> semester " + sem + " of " + program + "</b></html>";
                JOptionPane.showMessageDialog(null, errorMessage, "OK", JOptionPane.PLAIN_MESSAGE);
            }

        } catch (NullPointerException n) {
            String errorMessage = "<html><b style='font-size: 12px;'>Please Select Program..</b></html>";
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed

        if (jTable1.getSelectedRow() != -1) {
            int index = jTable1.getSelectedRow();
            int id = Integer.valueOf(jTable1.getValueAt(index, 0).toString());
            c.insertUpdateDeleteCourse('d', id, null, null, null, null, null, null);
            ManageCourseForm.jTable1.setModel(new DefaultTableModel(null, new Object[]{"Id", "Program", "Semester", "Course 1", "Course 2", "Course 3", "Course 4"}));
            c.fillStudentJTable(ManageCourseForm.jTable1);
            clearFields();
        } else {
            String errorMessage = "<html><b style='font-size: 12px;'>Please Select Field to delete..</b></html>";
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            try {
                int index = jTable1.getSelectedRow();
                int id = Integer.valueOf(jTable1.getValueAt(index, 0).toString());
                String program = cb_program.getSelectedItem().toString();
                int sem = Integer.parseInt(cb_sem.getSelectedItem().toString());

                String c1 = cb_c1.getSelectedItem().toString();
                String c2 = cb_c2.getSelectedItem().toString();
                String c3 = cb_c3.getSelectedItem().toString();
                String c4 = cb_c4.getSelectedItem().toString();

                if (!c.checkProgramSem('u', id, program, sem, c1, c2, c3, c4)) {
                    c.insertUpdateDeleteCourse('u', id, program, sem, c1, c2, c3, c4);
                    ManageCourseForm.jTable1.setModel(new DefaultTableModel(null, new Object[]{"Id", "Program", "Semester", "Course 1", "Course 2", "Course 3", "Course 4"}));
                    c.fillStudentJTable(ManageCourseForm.jTable1);

                    clearFields();
                } else {
                    String errorMessage = "<html><b style='font-size: 12px;'>Course Already Exist..</b></html>";
                    JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
                }

            } catch (NullPointerException n) {
                String errorMessage = "<html><b style='font-size: 12px;'>Please Select Field..</b></html>";
                JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            String errorMessage = "<html><b style='font-size: 12px;'>Please Select Field to edit..</b></html>";
            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_editActionPerformed
