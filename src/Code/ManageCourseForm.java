    private void jTable1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyReleased
        int index = jTable1.getSelectedRow();
        if (evt.getKeyCode() == KeyEvent.VK_UP || evt.getKeyCode() == KeyEvent.VK_DOWN) {
            cb_program.setSelectedItem(jTable1.getValueAt(index, 1).toString());
            String semValue = jTable1.getValueAt(index, 2).toString();
            for (int i = 0; i < cb_sem.getItemCount(); i++) {
                if (cb_sem.getItemAt(i).equals(semValue)) {
                    cb_sem.setSelectedIndex(i);
                    break;
                }
            }
            cb_c1.setSelectedItem(jTable1.getValueAt(index, 3).toString());
            cb_c2.setSelectedItem(jTable1.getValueAt(index, 4).toString());
            cb_c3.setSelectedItem(jTable1.getValueAt(index, 5).toString());
            cb_c4.setSelectedItem(jTable1.getValueAt(index, 6).toString());
        }

    }//GEN-LAST:event_jTable1KeyReleased

