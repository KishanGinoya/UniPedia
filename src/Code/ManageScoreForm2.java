    private void btn_add2btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add2btn_addActionPerformed

        try {
            String program = txt_program.getText();
            int sem = Integer.parseInt(txt_sem.getText().toString());
            String enr = txt_enr2.getText();
            int toenr = Integer.parseInt(enr);
            int c1m = Integer.parseInt(txt_mc1.getText());
            int c2m = Integer.parseInt(txt_mc2.getText());
            int c3m = Integer.parseInt(txt_mc3.getText());
            int c4m = Integer.parseInt(txt_mc4.getText());
            
//            // Limiting the score to maximum 100
//            c1m = Math.min(c1m, 100);
//            c2m = Math.min(c2m, 100);
//            c3m = Math.min(c3m, 100);
//            c4m = Math.min(c4m, 100);
        
            // Check if any score is above 100
            if (c1m > 100 || c2m > 100 || c3m > 100 || c4m > 100) {
                String warningMessage = "<html><b style='font-size: 12px;'>Please ensure that the scores are between 0 and 100...</b></html>";
                JOptionPane.showMessageDialog(null, warningMessage, "Warning", JOptionPane.WARNING_MESSAGE);
            }
            else{
                if (!(program.equals("Choose Program") && enr.equals("") && c1m == 0 && c2m == 0 && c3m == 0 && c4m == 0)) {
                    if (s.findId(toenr)) {
                        if (enr.matches("\\d+")) {
                            if (!s.checkMarksSem('i', null, program, sem, toenr, c1m, c2m, c3m, c4m)) {
                                s.insertUpdateDeleteScore('i', null, program, sem, toenr, c1m, c2m, c3m, c4m);
                                DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                                model.setRowCount(0); // Clear existing rows
                                s.fillStudentJTable(jTable3);
                                clearFields();
                            } else {
                                String errorMessage = "<html><b style='font-size: 12px;'>This Record Already Exist..<br> semester " + sem + " of " + program + "</b></html>";
                                JOptionPane.showMessageDialog(null, errorMessage, "OK", JOptionPane.PLAIN_MESSAGE);
                            }
                        } else {
                            String errorMessage = "<html><b style='font-size: 12px;'>String Value not allowed in marks...</b></html>";
                            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
                        }
                    }

                } else {
                    String errorMessage = "<html><b style='font-size: 12px;'>Please Fill Empty Field....</b></html>";
                    JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
                }
            }
            } catch (Exception e) {
                String errorMessage = "<html><b style='font-size: 12px;'>Please provide valid value..</b></html>";
                JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
            }


    }//GEN-LAST:event_btn_add2btn_addActionPerformed
