    private void btn_edit2btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_edit2btn_editActionPerformed
        if (jTable3.getSelectedRow() != -1) {
            try {
                int index = jTable3.getSelectedRow();
                int id = Integer.valueOf(jTable3.getValueAt(index, 0).toString());
                String program = txt_program.getText();
                int enr = Integer.valueOf(txt_enr2.getText());
                int sem = Integer.valueOf(txt_sem.getText().toString());

                int c1 = Integer.valueOf(txt_mc1.getText().toString());
                int c2 = Integer.valueOf(txt_mc2.getText().toString());
                int c3 = Integer.valueOf(txt_mc3.getText().toString());
                int c4 = Integer.valueOf(txt_mc4.getText().toString());
                
                // Check if any score exceeds 100
                if (c1 > 100 || c2 > 100 || c3 > 100 || c4 > 100) {
                    String warningMessage = "<html><b style='font-size: 12px;'>Please ensure that the scores are between 0 and 100...</b></html>";
                    JOptionPane.showMessageDialog(null, warningMessage, "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    if (s.findId(enr)) {
                        if (!s.checkMarksSem('u', id, program, sem, enr, c1, c2, c3, c4)) {
                            s.insertUpdateDeleteScore('u', id, program, sem, enr, c1, c2, c3, c4);
                            ManageScoreForm2.jTable3.setModel(new DefaultTableModel(null, new Object[]{"Id", "Program", "Semester", "Enrollment No", "Course 1 Marks", "Course 2 Marks", "Course 3 Marks", "Course 4 Marks"}));
                            s.fillStudentJTable(ManageScoreForm2.jTable3);

                            clearFields();
                        } else {
                            String errorMessage = "<html><b style='font-size: 12px;'>Score Already Given..</b></html>";
                            JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
                        }
                    }
                }

                } catch (Exception n) {
                    String errorMessage = "<html><b style='font-size: 12px;'>" + n.getMessage() + "</b></html>";
                    JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                String errorMessage = "<html><b style='font-size: 12px;'>Please Select Field to edit..</b></html>";
                JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_btn_edit2btn_editActionPerformed

