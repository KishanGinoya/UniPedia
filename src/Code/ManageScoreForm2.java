    private void btn_searchbtn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchbtn_searchActionPerformed
        // Check if the enrollment number field is empty
        if (txt_enr.getText().isEmpty()) {
            String message = "<html><b style='font-size: 12px;'>Please enter the enrollment number first!!!</b></html>";         
            JOptionPane.showMessageDialog(null, message , "Warning", JOptionPane.WARNING_MESSAGE);
            return; // Stop further execution
        }
        
        // Check if the enrollment number contains only digits
        String enrInput = txt_enr.getText().trim();
        if (!enrInput.matches("\\d+")) {
            String message = "<html><b style='font-size: 12px;'>Enrollment number should contain only digits!!!</b></html>";         
            JOptionPane.showMessageDialog(null, message , "Warning", JOptionPane.WARNING_MESSAGE);
            return; // Stop further execution
        }   
        
        int enr = Integer.valueOf(txt_enr.getText().toString());

        if (s.findId(enr)) {
            if(s.findCourse(s.sem, s.program)){
            try {
                int sem = s.sem;

                String program = s.program;

                s.getSubject();
                txt_c1.setText(s.c1);
                txt_c2.setText(s.c2);
                txt_c3.setText(s.c3);
                txt_c4.setText(s.c4);
                txt_program.setText(program);
                txt_sem.setText(sem + "");
                txt_enr2.setText(enr + "");
                

            } catch (Exception e) {
                String errorMessage = "<html><b style='font-size: 12px;'>" + e.getMessage() + "</b></html>";
                JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
        }
    }//GEN-LAST:event_btn_searchbtn_searchActionPerformed

