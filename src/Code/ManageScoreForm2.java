    private void jTable3jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3jTable1MouseClicked
        int index = jTable3.getSelectedRow();
            if (index == selectedRowIndex) {
           // Deselect the row
           jTable3.getSelectionModel().clearSelection();
           // Clear form data
           clearFields();
           // Reset selectedRowIndex
           selectedRowIndex = -1;
       } else {
            String program = jTable3.getValueAt(index, 1).toString();
            int sem = Integer.parseInt(jTable3.getValueAt(index, 2).toString());
            txt_program.setText(program);
            txt_sem.setText(sem+"");
            int enr = Integer.parseInt(jTable3.getValueAt(index, 3).toString());

            txt_enr.setText(enr+"");
            int semValue = Integer.parseInt(jTable3.getValueAt(index, 2).toString());

            try {
                Connection con = MyConnection.getConnection();
                PreparedStatement pr = con.prepareStatement("select * from coursedetails where semester=? and program=?");
                pr.setInt(1, semValue);
                pr.setString(2, program);

                ResultSet rs = pr.executeQuery();
                if (rs.next()) {
                    txt_c1.setText(rs.getString("c1"));
                    txt_c2.setText(rs.getString("c2"));
                    txt_c3.setText(rs.getString("c3"));
                    txt_c4.setText(rs.getString("c4"));
                }
            } catch (Exception e) {
                String errorMessage = "<html><b style='font-size: 12px;'>" + e.getMessage() + "</b></html>";
                JOptionPane.showMessageDialog(null, errorMessage, "Error", JOptionPane.WARNING_MESSAGE);
            }
            txt_enr2.setText(jTable3.getValueAt(index, 3).toString());
            txt_mc1.setText(jTable3.getValueAt(index, 4).toString());
            txt_mc2.setText(jTable3.getValueAt(index, 5).toString());
            txt_mc3.setText(jTable3.getValueAt(index, 6).toString());
            txt_mc4.setText(jTable3.getValueAt(index, 7).toString());
            
            // Set selectedRowIndex
            selectedRowIndex = index;
        }

    }//GEN-LAST:event_jTable3jTable1MouseClicked

